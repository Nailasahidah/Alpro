#include <iostream>
using namespace std;

 int main(){
     for(int i = 1; i <= 15; i ++){
         if (i % 2 != 0) {
             cout << i << " ";
         }
     }
     return 0;
 }

/*
Buatlah program yang  menampilkan bilangan ganjil dari 1 hingga 15 
menggunakan perulangan for.
*/